package ejerciciosVariables;

public class Ejercicio4 {
   public static void main(String[] args) {
      // Java considera que char son caracteres ascii
      // Al usar char se deben usar comillas simples sino devuelve error
      char letraA = 'A';
      char letraE = 'E';
      char letraI = 'I';
      char letraO, letraU;
      letraO = 'O';
      letraU = 'U';
      System.out.println("" + letraA + letraE + letraI + letraO + letraU );
   }
}
