package ejerciciosVariables;

public class Ejercicio1 {
   public static void main(String[] args) throws Exception {
      double num1 = 14;
      double num2 = 6;

      System.out.println("La suma de " + num1 + " y " + num2 + " es: " + ( num1 + num2 ));
      System.out.println("La resta de " + num1 + " y " + num2 + " es: " + ( num1 - num2 ));
      System.out.println("La multiplicacion de " + num1 + " y " + num2 + " es: " + ( num1 * num2 ));
      System.out.println("La division de " + num1 + " y " + num2 + " es: " + ( num1 / num2 ));
      System.out.println("El resto de la division de " + num1 + " y " + num2 + " es: " + ( num1 % num2 ));
   }
}
